namespace RentCars.Commons.Enums
{
    /// <summary>
    /// Represents the type of engine a vehicle can have.
    /// </summary>
    public enum EngineType
    {
        /// <summary>
        /// Engine powered by diesel fuel.
        /// </summary>
        Diesel,

        /// <summary>
        /// Engine powered by petrol (gasoline) fuel.
        /// </summary>
        Petrol,

        /// <summary>
        /// Engine powered by electricity.
        /// </summary>
        Electric,

        /// <summary>
        /// Engine that combines both an internal combustion engine and an electric motor.
        /// </summary>
        Hybrid
    }
}